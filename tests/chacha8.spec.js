    it('transform a string with chacha8 algorithm based on a nounce and key', function() {
        for (let i = 0; i < testdata.length; i++) {
            let d = testdata[i];
            let expected = d['cipher_text'];
            let cipherResult = new Chacha8(d['key'], d['iv'], d['plain_text']);

            expect(expected).to.eql(cipherResult);
        }
    });

    it('use chacha algo with wrong nounce length', function() {
        
        let d = testdata[0];
        let wrongIv = new Int16Array([0x28, 0x8f, 0xf6, 0x5d, 0xc4, 0x2b, 0x92]);


        expect(() => new Chacha8(d['key'], wrongIv, d['plain_text'])).to.throw('wrong nonce length (needs to be 8)');
    });

    it('cut cipher', function() {
        
        let d = testdata[0];
        let expected = new Int32Array([0x65]);
        let cipherResult = new Chacha8(d['key'], d['iv'], d['plain_text'], 1);

        expect(expected).to.eql(cipherResult);
    });
